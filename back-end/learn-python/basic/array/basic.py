numbers = [10, 20, 30, 40, 50]

print(numbers[0])  # Output: 10 (Phần tử đầu tiên)
print(numbers[3])  # Output: 40 (Phần tử thứ tư)

print(numbers[-1])  # Output: 50 (Phần tử cuối cùng)
print(numbers[-2])  # Output: 40 (Phần tử gần cuối)

# start index default = 0, end index default = len(arr) - 1
print(numbers[1:4])  # Output: [20, 30, 40] (Từ chỉ số 1 đến 3)

print(20 in numbers)  # Output: True
print(40 not in numbers)  # Output: True
